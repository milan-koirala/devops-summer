

# Addition
def add(a, b):
    return a + b


# Subtraction
def subtract(a, b):
    return a - b


# Multiplication
def multiply(a, b):
    return a * b


# Division
def divide(a, b):
    if b == 0:
        return "Error: Cannot divide by zero"
    return a / b


# Power
def power(a, b):
    return a ** b


# Modulus
def modulus(a, b):
    return a % b
